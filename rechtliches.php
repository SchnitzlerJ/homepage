<h1>Bu&szlig;geldh&ouml;he</h1>
<p>
In der Regel werden nur geringe Bu&szlig;gelder verh&auml;ngt, wenn Rad- und Fu&szlig;wege zugeparkt werden. Hierbei w&auml;hlen die Beamten oft eine m&ouml;glichst g&uuml;nstige Ordnungswidrigkeit!
</p>
<p>
Die St&auml;dtische Verkehrspolizei ahndet Verst&ouml;&szlig;e nach dem bundesweit g&uuml;ltigen <a href="https://www.gesetze-im-internet.de/bkatv_2013/anlage.html">Bu&szlig;geldkatalog</a><sup><a href="quellen_1">1</a></sup>. Hierbei sind insbesondere die Nummern 52a (parken auf Geh- und Radwegen) sowie 54a (parken auf Schutzstreifen) interessant. Die Bu&szlig;gelder reichen bei einem Geh-/Radweg von 20&euro; (52a - parken ohne Behinderung) &uuml;ber 30&euro; (52a.1 - parken mit Behinderung, liegt meist vor!) bis hin zu 30&euro; (52a.2 - parken l&auml;nger als 1 Stunde) und 35&euro; (52a.2.1 - parken l&auml;nger als 1 Stunde mit Behinderung).
</p>
<p>
H&auml;fig wird eine Behinderung zwar erkannt, aber nicht dem Bu&szlig;geldkatalog entsprechend geahndet (nur 10-20&euro; Bu&szlig;geld). Hier lohnt es sich das Gespr&auml;ch mit den Beamten zu suchen.
</p>

